def load_cache() -> dict[str, dict[str, str]]:
    """Загрузить кэш SMILES/CAS из файла."""
    if CACHE_FILE.exists():
        try:
            data: dict[str, dict[str, str]] = json.loads(CACHE_FILE.read_text(encoding="utf-8"))
            log.info("Кэш загружен: %d записей из %s", len(data), CACHE_FILE.name)
            return data
        except Exception as exc:
            log.warning("Не удалось загрузить кэш: %s", exc)
    return {}


def save_cache(cache: dict[str, dict[str, str]]) -> None:
    """Сохранить кэш в файл."""
    try:
        CACHE_FILE.write_text(json.dumps(cache, ensure_ascii=False, indent=2), encoding="utf-8")
    except Exception as exc:
        log.warning("Не удалось сохранить кэш: %s", exc)


# ─────────────────────────────────────────────────────────────────────────────
# HTTP-помощник с retry и экспоненциальным backoff
# ─────────────────────────────────────────────────────────────────────────────

# Флаг: если PubChem недоступен — пропускать его для всей сессии
_pubchem_ok: bool = True


def _http_get(url: str, timeout: int = 12, retries: int = 2) -> "requests.Response | None":
    """GET с повторными попытками. Возвращает None при неустранимой ошибке сети."""
    for attempt in range(retries):
        try:
            r = requests.get(url, timeout=timeout)
            if r.status_code == 404:
                return r          # 404 — вещество не найдено, возвращаем как есть
            r.raise_for_status()
            return r
        except requests.exceptions.ConnectionError as exc:
            log.warning("Нет соединения (попытка %d/%d): %s", attempt + 1, retries, exc)
        except requests.exceptions.Timeout:
            log.warning("Таймаут (попытка %d/%d): %s", attempt + 1, retries, url)
        except requests.exceptions.HTTPError as exc:
            log.warning("HTTP ошибка: %s", exc)
            break  # при HTTP-ошибках (кроме 429) повторять бессмысленно
        if attempt < retries - 1:
            wait = REQUEST_DELAY * (2 ** attempt)
            log.info("  Повтор через %.1f с...", wait)
            time.sleep(wait)
    return None


# ─────────────────────────────────────────────────────────────────────────────
# PubChem API
# ─────────────────────────────────────────────────────────────────────────────

def pubchem_get_properties(name: str) -> dict[str, str] | None:
    """Запросить Canonical SMILES, IUPACName, MolecularFormula по имени."""
    url = (
        f"{PUBCHEM_BASE}/compound/name/{requests.utils.quote(name)}"
        "/property/CanonicalSMILES,IUPACName,MolecularFormula/JSON"
    )
    r = _http_get(url)
    if r is None or r.status_code == 404:
        return None
    try:
        props = r.json()["PropertyTable"]["Properties"][0]
        return {
            "smiles": props.get("CanonicalSMILES", ""),
            "iupac": props.get("IUPACName", ""),
            "molecular_formula": props.get("MolecularFormula", ""),
            "pubchem_cid": str(props.get("CID", "")),
        }
    except Exception as exc:
        log.warning("PubChem parse error for '%s': %s", name, exc)
        return None


def pubchem_get_cas(cid: str) -> str:
    """Получить CAS-номер через синонимы PubChem."""
    url = f"{PUBCHEM_BASE}/compound/cid/{cid}/synonyms/JSON"
    r = _http_get(url)
    if r is None:
        return ""
    try:
        synonyms: list[str] = r.json()["InformationList"]["Information"][0]["Synonym"]
        cas_pattern = re.compile(r"^\d{2,7}-\d{2}-\d$")
        for s in synonyms:
            if cas_pattern.match(s):
                return s
    except Exception as exc:
        log.warning("PubChem CAS error for CID %s: %s", cid, exc)
    return ""


# ─────────────────────────────────────────────────────────────────────────────
# NCI CIR — fallback-парсер если PubChem недоступен
# ─────────────────────────────────────────────────────────────────────────────

CIR_BASE = "https://cactus.nci.nih.gov/chemical/structure"


def cir_get_data(name: str) -> dict[str, str]:
    """Запросить SMILES и CAS через NCI Chemical Identifier Resolver."""
    result: dict[str, str] = {}
    encoded = requests.utils.quote(name)

    r = _http_get(f"{CIR_BASE}/{encoded}/smiles", retries=2)
    if r and r.status_code == 200:
        smiles = r.text.strip()
        if smiles and "\n" not in smiles:   # CIR может вернуть несколько строк
            result["smiles"] = smiles
        elif smiles:
            result["smiles"] = smiles.splitlines()[0].strip()

    if result.get("smiles"):
        r = _http_get(f"{CIR_BASE}/{encoded}/cas", retries=2)
        if r and r.status_code == 200:
            lines = [ln.strip() for ln in r.text.strip().splitlines() if ln.strip()]
            if lines:
                result["cas"] = lines[0]

    return result


# ─────────────────────────────────────────────────────────────────────────────
# Основная функция получения данных: кэш → PubChem → CIR
# ─────────────────────────────────────────────────────────────────────────────

def fetch_compound_data(
    name_en: str,
    cache: dict[str, dict[str, str]],
    cas_hint: str = "",
) -> dict[str, str]:
    """
    Пытается получить SMILES/IUPAC/CAS/formula для вещества.

    Стратегия:
      1. Проверяет локальный кэш (smiles_cache.json).
      2. Запрашивает PubChem (если доступен).
      3. Если SMILES нет — запрашивает NCI CIR по имени.
      4. Если SMILES нет и есть cas_hint — запрашивает CIR по CAS.
      5. Результат сохраняется в кэш.
    """
    global _pubchem_ok

    # 1. Кэш
    if name_en in cache:
        cached = cache[name_en]
        log.info("  Кэш: %s → %s", name_en, cached.get("smiles", "—")[:40])
        return cached

    result: dict[str, str] = {"smiles": "", "iupac": "", "molecular_formula": "", "cas": ""}

    # 2. PubChem (только если он доступен в этой сессии)
    if not _pubchem_ok:
        log.info("  PubChem недоступен → сразу CIR: %s", name_en)

    props = pubchem_get_properties(name_en) if _pubchem_ok else None
    if props:
        cas = ""
        if props.get("pubchem_cid"):
            time.sleep(REQUEST_DELAY)
            cas = pubchem_get_cas(props["pubchem_cid"])
        result = {
            "smiles": props["smiles"],
            "iupac": props["iupac"],
            "molecular_formula": props["molecular_formula"],
            "cas": cas,
        }
        log.info("  PubChem OK: %s → %s", name_en, result["smiles"][:50] if result["smiles"] else "—")
    else:
        log.info("  PubChem: не найдено → пробую CIR: %s", name_en)

    # 3. CIR по имени (если нет SMILES)
    if not result["smiles"]:
        time.sleep(REQUEST_DELAY)
        cir = cir_get_data(name_en)
        if cir.get("smiles"):
            result["smiles"] = cir["smiles"]
            result["cas"] = result["cas"] or cir.get("cas", "")
            log.info("  CIR(name) OK: %s → %s", name_en, result["smiles"][:50])

    # 4. CIR по CAS-номеру (последний шанс, если есть cas_hint)
    if not result["smiles"] and cas_hint:
        time.sleep(REQUEST_DELAY)
        log.info("  CIR(CAS=%s) → пробую для '%s'", cas_hint, name_en)
        cir = cir_get_data(cas_hint)
        if cir.get("smiles"):
            result["smiles"] = cir["smiles"]
            result["cas"] = result["cas"] or cas_hint
            log.info("  CIR(CAS) OK: %s → %s", name_en, result["smiles"][:50])

    if not result["smiles"]:
        log.warning("  НЕ НАЙДЕНО нигде: '%s'", name_en)

    # 5. Сохранить в кэш
    cache[name_en] = result
    save_cache(cache)

    time.sleep(REQUEST_DELAY)
    return result


