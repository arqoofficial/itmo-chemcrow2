def make_id(name_en: str) -> str:
    """Генерировать id из английского названия."""
    return re.sub(r"[^a-z0-9]+", "_", name_en.lower()).strip("_")


def build_record(
    entry: dict[str, Any],
    list_type: str,  # "poisonous" | "potent"
    cache: dict[str, dict[str, str]],
) -> dict[str, Any]:
    name_en: str = entry["name_en"]
    name_ru: str = entry["name_ru"]
    skip: bool = entry.get("skip_pubchem", False)

    if skip:
        log.info("Пропуск (комбинация/растение): %s", name_ru)
        chem: dict[str, str] = {}
    else:
        log.info("Получаю данные: %s (%s)", name_en, name_ru)
        chem = fetch_compound_data(name_en, cache, cas_hint=entry.get("cas_hint", ""))

    warnings = STANDARD_WARNINGS[list_type] + entry.get("extra_warnings", [])
    if entry.get("combination_note"):
        warnings.append(f"Состав: {entry['combination_note']}")

    record: dict[str, Any] = {
        "id": make_id(name_en),
        "names": [name_ru, name_en],
        "iupac": chem.get("iupac", ""),
        "smiles": chem.get("smiles", ""),
        "cas": chem.get("cas", ""),
        "molecular_formula": chem.get("molecular_formula", ""),
        "pkkn_list": list_type,
        "is_combination": skip,
        "hazard_categories": CATEGORY_MAP[list_type].copy(),
        "severity": SEVERITY_MAP[list_type],
        "safety_warnings": warnings,
        "description": (
            f"Вещество из Списка {'№2 ядовитых' if list_type == 'poisonous' else '№1 сильнодействующих'} "
            "веществ ПККН (ред. 16.03.2006)."
        ),
    }

    # Обогащаем категории на основе ключевых слов в предупреждениях
    combined = " ".join(warnings).lower()
    if "канцероген" in combined:
        record["hazard_categories"].append("CARCINOGEN")
    if "взрыво" in combined or "самовозгор" in combined:
        record["hazard_categories"].append("EXPLOSIVE")
    if "воспламен" in combined or "легкогор" in combined:
        record["hazard_categories"].append("FLAMMABLE")
    if "нейротоксин" in combined or "нейротокс" in combined or "поражает цнс" in combined:
        record["hazard_categories"].append("NEUROTOXIN")
    if "ожог" in combined or "разъедает" in combined or "едкий" in combined or "едкое" in combined:
        record["hazard_categories"].append("CORROSIVE")
    if "химическое оружие" in combined or "отравляющее вещество" in combined:
        record["hazard_categories"].append("CHEMICAL_WEAPON")
    if "прекурсор" in combined:
        record["hazard_categories"].append("PRECURSOR_CONTROLLED")

    # Убираем дубликаты, сохраняя порядок
    seen: set[str] = set()
    record["hazard_categories"] = [
        c for c in record["hazard_categories"] if not (c in seen or seen.add(c))  # type: ignore[func-returns-value]
    ]

    return record


