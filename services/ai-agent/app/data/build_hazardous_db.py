def _probe_pubchem() -> bool:
    """Быстрая проверка доступности PubChem (1 запрос, 5 сек таймаут)."""
    try:
        r = requests.get(
            f"{PUBCHEM_BASE}/compound/name/water/property/CanonicalSMILES/JSON",
            timeout=5,
        )
        return r.status_code == 200
    except Exception:
        return False


def main() -> None:
    global _pubchem_ok
    cache = load_cache()

    log.info("Проверяю доступность PubChem...")
    _pubchem_ok = _probe_pubchem()
    if _pubchem_ok:
        log.info("PubChem доступен — будет использован как основной источник")
    else:
        log.warning("PubChem недоступен — используем только NCI CIR")

    results: list[dict[str, Any]] = []

    log.info("=== Обрабатываю Список №1 СИЛЬНОДЕЙСТВУЮЩИХ (%d веществ) ===", len(POTENT))
    for entry in POTENT:
        results.append(build_record(entry, "potent", cache))

    log.info("=== Обрабатываю Список №2 ЯДОВИТЫХ (%d веществ) ===", len(POISONOUS))
    for entry in POISONOUS:
        results.append(build_record(entry, "poisonous", cache))

    OUTPUT_FILE.write_text(
        json.dumps(results, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    log.info("Готово: %d записей → %s", len(results), OUTPUT_FILE)

    # Статистика
    found_smiles = sum(1 for r in results if r["smiles"])
    skipped = sum(1 for r in results if r["is_combination"])
    log.info(
        "Покрытие SMILES (PubChem+CIR): %d / %d (%.0f%%) | Комбинации/смеси: %d",
        found_smiles,
        len(results) - skipped,
        100 * found_smiles / max(len(results) - skipped, 1),
        skipped,
    )


if __name__ == "__main__":
    main()
