def flatten_queries(queries: dict[str, list[str]]) -> list[tuple[str, str]]:
    """Convert mapping doc_id -> queries to flat list of (doc_id, query)."""
    pairs: list[tuple[str, str]] = []
    for doc_id, items in queries.items():
        for query in items:
            pairs.append((doc_id, query))
    return pairs


def flatten_chunks(chunks: dict[str, dict]) -> list[tuple[str, str, str]]:
    """Convert chunks dict to flat list of (chunk_id, text, query)."""
    triples: list[tuple[str, str, str]] = []
    for chunk_id, data in chunks.items():
        text = data["text"]
        for query in data["queries"]:
            triples.append((chunk_id, text, query))
    return triples
